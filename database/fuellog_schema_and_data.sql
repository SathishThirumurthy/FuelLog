-- ── TABLE: cars ─────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS cars (
    id          VARCHAR(50)  PRIMARY KEY,
    name        VARCHAR(100) NOT NULL,
    country     CHAR(2)      NOT NULL DEFAULT 'IN',
    purchased   DATE,
    sold        DATE,
    note        TEXT,
    reg         VARCHAR(30),
    active      BOOLEAN      DEFAULT FALSE,
    created_at  TIMESTAMP    DEFAULT CURRENT_TIMESTAMP
);

COMMENT ON TABLE  cars             IS 'Registered vehicles';
COMMENT ON COLUMN cars.country     IS 'IN = India, US = United States';
COMMENT ON COLUMN cars.purchased   IS 'Date car was purchased';
COMMENT ON COLUMN cars.sold        IS 'Date car was sold, NULL if still owned';
COMMENT ON COLUMN cars.active      IS 'Currently selected car for new entries';


-- ── TABLE: fuel_entries ──────────────────────────────────────
CREATE TABLE IF NOT EXISTS fuel_entries (
    id          VARCHAR(50)     PRIMARY KEY,
    car_id      VARCHAR(50)     NOT NULL REFERENCES cars(id) ON DELETE CASCADE,
    entry_date  DATE            NOT NULL,
    odometer_km NUMERIC(10,2)   NOT NULL,
    fuel_qty    NUMERIC(8,2)    NOT NULL,
    price_per_unit NUMERIC(8,2) DEFAULT 0,
    distance_km NUMERIC(10,2),
    mileage     NUMERIC(8,2),
    amount      NUMERIC(10,2),
    place       VARCHAR(200),
    created_at  TIMESTAMP       DEFAULT CURRENT_TIMESTAMP
);

COMMENT ON TABLE  fuel_entries              IS 'Fuel fill-up records';
COMMENT ON COLUMN fuel_entries.odometer_km  IS 'Odometer reading at fill-up (km or miles)';
COMMENT ON COLUMN fuel_entries.fuel_qty     IS 'Litres or gallons filled';
COMMENT ON COLUMN fuel_entries.price_per_unit IS 'Price per litre or gallon';
COMMENT ON COLUMN fuel_entries.distance_km  IS 'Distance since last fill-up';
COMMENT ON COLUMN fuel_entries.mileage      IS 'Fuel efficiency (km/L or MPG)';
COMMENT ON COLUMN fuel_entries.amount       IS 'Total amount paid (fuel_qty x price_per_unit)';
COMMENT ON COLUMN fuel_entries.place        IS 'Fuel station name or location';


-- ── TABLE: service_entries ───────────────────────────────────
CREATE TABLE IF NOT EXISTS service_entries (
    id           VARCHAR(50)   PRIMARY KEY,
    car_id       VARCHAR(50)   NOT NULL REFERENCES cars(id) ON DELETE CASCADE,
    service_date DATE          NOT NULL,
    odometer_km  NUMERIC(10,2),
    amount       NUMERIC(10,2),
    remarks      TEXT,
    created_at   TIMESTAMP     DEFAULT CURRENT_TIMESTAMP
);

COMMENT ON TABLE  service_entries             IS 'Vehicle service and maintenance records';
COMMENT ON COLUMN service_entries.odometer_km IS 'Odometer reading at service';
COMMENT ON COLUMN service_entries.amount      IS 'Total service cost';
COMMENT ON COLUMN service_entries.remarks     IS 'Description of work done';


