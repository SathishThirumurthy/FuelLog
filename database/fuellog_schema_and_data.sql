-- Quick summary per car
CREATE OR REPLACE VIEW v_car_summary AS
SELECT
    c.id,
    c.name,
    c.country,
    c.purchased,
    c.sold,
    c.active,
    COUNT(DISTINCT f.id)                          AS total_fillups,
    ROUND(SUM(f.fuel_qty)::NUMERIC, 1)            AS total_fuel,
    ROUND(SUM(f.amount)::NUMERIC, 0)              AS total_fuel_spend,
    ROUND(AVG(f.mileage)::NUMERIC, 1)             AS avg_mileage,
    MAX(f.odometer_km) - MIN(f.odometer_km)       AS total_distance,
    COUNT(DISTINCT s.id)                          AS total_services,
    ROUND(SUM(s.amount)::NUMERIC, 0)              AS total_svc_spend
FROM cars c
LEFT JOIN fuel_entries    f ON f.car_id = c.id
LEFT JOIN service_entries s ON s.car_id = c.id
GROUP BY c.id, c.name, c.country, c.purchased, c.sold, c.active;


-- Yearly fuel summary
CREATE OR REPLACE VIEW v_yearly_fuel AS
SELECT
    f.car_id,
    c.name                                        AS car_name,
    EXTRACT(YEAR FROM f.entry_date)::INT          AS year,
    COUNT(*)                                      AS fillups,
    ROUND(SUM(f.fuel_qty)::NUMERIC, 1)            AS total_fuel,
    ROUND(SUM(f.amount)::NUMERIC, 0)              AS total_spend,
    ROUND(AVG(f.mileage)::NUMERIC, 1)             AS avg_mileage,
    ROUND(AVG(f.price_per_unit)::NUMERIC, 2)      AS avg_price
FROM fuel_entries f
JOIN cars c ON c.id = f.car_id
WHERE f.mileage IS NOT NULL
GROUP BY f.car_id, c.name, EXTRACT(YEAR FROM f.entry_date)
ORDER BY f.car_id, year;


-- Top fuel stations
CREATE OR REPLACE VIEW v_top_stations AS
SELECT
    car_id,
    place,
    COUNT(*)                                      AS visit_count,
    ROUND(SUM(amount)::NUMERIC, 0)                AS total_spend,
    ROUND(AVG(mileage)::NUMERIC, 1)               AS avg_mileage
FROM fuel_entries
WHERE place IS NOT NULL AND place <> ''
GROUP BY car_id, place
ORDER BY visit_count DESC;


